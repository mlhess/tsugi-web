									<div><h2>Using these tools from an LTI 1.x Consumer</h2></div>
								</div>
								<div class="mb-2" align=justify>
                                 <p>Depending on the administrator of the Tsugi server, you may be able to get an LTI key and secret for the use of the tools on the server. Tsugi has a capability to let you log in and request keys that can be enabled by the sytem owner.</p>
<p>Tsugi supports both LTI 1.x and has support for LTI 2.0 but most uses of these tools in an LMS is LTI 1.x. So you most likely will need an LTI 1.x key.</p>
<p>You can plug your key, secret, and launch url (see below) into your LMS or test using any LTI Consumer harness such as the one I use at:</p>
<a href=https://online.dr-chuck.com/sakai-api-test/lms.php>https://online.dr-chuck.com/sakai-api-test/lms.php</a>
<p>Here is a list of the tools installed on most Tsugi servers by default.</p>
<h2>Social Peer Grader</h2>
<p>This is my very simple social peer grader that I use for low-stakes assessment in my on-campus and Coursera classes. It is generally a few images and/or a bit of text with a simple grading rubric. Here is the URL for the peer grading:</p>
<a href=https://lti-tools.dr-chuck.com/tsugi/mod/peer-grade/index.php>https://lti-tools.dr-chuck.com/tsugi/mod/peer-grade/index.php</a>
<p>Make sure to log in first as the instructor and configure the peer-grader and save the configuration. Otherwise students will see the "not yet configured" message. The default configuration is a blob of JSON that is OK to use use as is for testing. It asks for two images and some text. It requires two other peer-evals for two points and your peers give you from 0-6 points. It is hard to see it in action unless you make a few student acounts or test from Tsugi developer mode.</p>
<p>Instructors can view various dashboards, trigger regrading, process flagged items, and many other features.</p>
<h2>Class Map</h2>
<p>This is a simple map where students can place themselves and decide how much data they want to reveal. Its launch url is of the form:</p>
<a href=https://lti-tools.dr-chuck.com/tsugi/mod/map/index.php>https://lti-tools.dr-chuck.com/tsugi/mod/map/index.php</a>
<p>Each student can control how much information they reveal on the map. The instructor can see all student data on the map.</p>
<h2>Attendance Tool</h2>
<p>This is a simple tool where the teacher sets a code and the students need to enter the code. The teacher can see who entered the code, when it was entered, and what IP was used to enter the code. This is trivially useful, really just a starting point for a more sophisticated attendance application.</p>
<a href=https://lti-tools.dr-chuck.com/tsugi/attend/map/index.php>https://lti-tools.dr-chuck.com/tsugi/attend/map/index.php</a>
<h2>Python Autograder</h2>
<p>This is the autograder associated with my Coursera Programming for Everybody and Python for Informatics text book. This tool launches with the following URL:</p>
<a href=https://lti-tools.dr-chuck.com/tsugi/mod/pythonauto/auto.php>https://lti-tools.dr-chuck.com/tsugi/mod/pythonauto/auto.php</a>
<p>Once you launch, you can go into the settings screen and select the exercise for the placement as well as due date, close action, etc.</p>
<p>For the exercises, Tsugi will send grades back to the LMS if the LMS will accept the grades.</p>
<h2>Gradebook Tool</h2>
<p>After you have a bunch of graded items in the external server you might want to let the student see the current status of their grades hosted on the LTI server. Also you might want your teaching assistants to be able to go through the student grade books and see what is going on. This only sees the grades on the LTI server in a class across all the links in the class.</p>
<p>It also double checks to see if the LMS sense of the grade is the same as the LTI server and fixes errors in the LMS grade automatially. In effect it quietly repairs lost grades in the LMS when the student checks.<p>
<a href=https://lti-tools.dr-chuck.com/tsugi/grades/map/index.php>https://lti-tools.dr-chuck.com/tsugi/grades/map/index.php</a>
<h2>Rock Paper Scissors</h2>
<p>This is a simple multi-player game of RPS. My main goal of this is to demonstrate transactions in sample code. But it is a bit of fun and has a little leaderboard.</p>
<a href=https://lti-tools.dr-chuck.com/tsugi/attend/map/index.php>https://lti-tools.dr-chuck.com/tsugi/attend/map/index.php</a>
<p>I also used this as part of my sample Android mobile application. but that is another story.</p>
<h2>Summary</h2>
<p>Note that if you play with the developer interface, some of the sample bits are broken on purpose. They are their to be fixed as training exercises in a Tsugi boot camp or class.</p>
<p>As you can see, this would greatly benefit from LTI 2.0 - because you would never look at a page like this with this nasty plumbing details. But that takes time - but I should be working on it soon.</p>
<p>As this is emergent software, comments, suggestions, and pull requests are always welcome.</p>
