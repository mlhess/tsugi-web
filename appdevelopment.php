                                    <p>

<p>There are two ways to use the Tsugi library/framework:</p>

<li>
<p>
You can build a "Tsugi Module" from scratch following all of the Tsugi style guidance, using the Tsugi browser environment, and making full use of the Tsugi framework. We have starter code for <a href="https://github.com/csev/tsugi-php-module">Bulding a Tsugi Module</a>
</p>
</li>
<li>
<p>
You can use Tsugi more like a library and add it to an existing application. We have example code showing how you might <a href="https://github.com/csev/tsugi-php-standalone">Use Tsugi With an Existing Application</a>
</p>
</li>
<li>
<p>Both of these approaches depend on the <a href="https://github.com/csev/tsugi">Tsugi Devloper/Admin Console</a> for database configuration, setup, developer test harness, CASA support, Content Item Support, etc.
</p>
</li>
