                                    <p><b>Installation Video</b></p>

                                    <h2><b>Pre-Requisites</b></h2>
<br>
                                    <ul> <li>Install GIT so that it works at the command prompt.</li>

                                    <li>Install a PHP/MySQL Environment like XAMPP / MAMP following the instructions at:</li></ul>

<p><a href = "http://www.php-intro.com/install.php" target="_blank"><u>PHP-Intro</u></a></p>

<p><b>To install this software follow these steps:</b></p>

<h2>Installation</h2>
<br>
<ul>
	<li>Check the code out from GitHub and put it in a directory where your web server can read it</li>

<p>git clone <a href="https://github.com/csev/tsugi.git" target="_blank">https://github.com/csev/tsugi.git</a></p>
<li>Create a database and get authentication info for the database</li>

<p>CREATE DATABASE tsugi DEFAULT CHARACTER SET utf8;
GRANT ALL ON tsugi.* TO 'ltiuser'@'localhost' IDENTIFIED BY 'ltipassword';
GRANT ALL ON tsugi.* TO 'ltiuser'@'127.0.0.1' IDENTIFIED BY 'ltipassword';</p>
<li>Copy the file config-dist.php to config.php and edit the file to put in the appropriate values. Make sure to change all the secrets. If you are just getting started turn on DEVELOPER mode so you can launch the tools easily. Each of the fields is documented in the config-dist.php file - here is some additional documentation on the configuration values:</p>
</li>
<p><a href="http://do1.dr-chuck.com/tsugi/phpdoc/classes/Tsugi.Config.ConfigInfo.html" target="_blank">http://do1.dr-chuck.com/tsugi/phpdoc/classes/Tsugi.Config.ConfigInfo.html</a></p>

<li>Go to the main page, and click on "Admin" to make all the database tables - you will need the Admin password you just put into config.php If all goes well, lots of tables should be created. You can run upgrade.php more than once - it will automatically detect that it has been run.</li>

<li>At that point you can play with and/or develop new tools</li>
</ul>
<p>Note: Make sure that none of the folders in the path to the tsugi folder have any spaces in them. You may get signature errors if you use folders with blanks in them.</p>

<h2>MAMP NOTES (Macintosh)</h2>
<br>

<p>cd /Applications/MAMP/htdocs/</p>
<p>git clone https://github.com/csev/tsugi.git</p>
<p>cd tsugi</p>
<p>cp config-dist.php config.php</p>

<p>edit config.php using a text editor - some values</p>

<p>Make sure to change $wwwroot to reflect where your server is </p>
<p>hosted or the CSS files will not be loaded.</p>

<p>$wwwroot = 'http://localhost:8888/tsugi';</p>
<p>$CFG->pdo = 'mysql:host=127.0.0.1;port=8889;dbname=tsugi';</p>
<p>$CFG->dbprefix  = '';</p>
<p>$CFG->adminpw = ....;</p>

<p>Make a database using PhpMyAdmin:</p>

<p>CREATE DATABASE tsugi DEFAULT CHARACTER SET utf8;</p>
<p>GRANT ALL ON tsugi.* TO 'ltiuser'@'localhost' IDENTIFIED BY 'ltipassword';</p>
<p>GRANT ALL ON tsugi.* TO 'ltiuser'@'127.0.0.1' IDENTIFIED BY 'ltipassword';</p>

<p>Visit  http://localhost:8888/tsugi and go to 'Admin' and enter the</p>
<p>adminpw to automatically create all necessary tables.</p>
<h2>XAMPP NOTES (Windows)</h2>
<br>

<p>cd \xampp\htdocs</p>
<p>git clone https://github.com/csev/tsugi.git</p>
<p>cd tsugi</p>
<p>copy config-dist.php config.php</p>

<p>edit config.php using a text editor - some values</p>

<p>Make sure to change $wwwroot to reflect where your server is </p>
<p>hosted or the CSS files will not be loaded.</p>

<p>$wwwroot = 'http://localhost/tsugi';</p>
<p>$CFG->dbprefix  = '';</p>
<p>$CFG->adminpw = ....;</p>

<p>Make a database using PhpMyAdmin:</p>

<p>CREATE DATABASE tsugi DEFAULT CHARACTER SET utf8;</p>
<p>GRANT ALL ON tsugi.* TO 'ltiuser'@'localhost' IDENTIFIED BY 'ltipassword';</p>
<p>GRANT ALL ON tsugi.* TO 'ltiuser'@'127.0.0.1' IDENTIFIED BY 'ltipassword';</p>

<p>Visit  http://localhost/tsugi and go to 'Admin' and enter the</p>
<p>adminpw to automatically create all necessary tables.</p>
<p>If you are setting this up on some variation of Linux, the Macintosh instructions will be the most help.</p>
